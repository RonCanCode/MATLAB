% First Partials:
dXdt = gradient(xpos, time);
dYdt = gradient(ypos, time);
dZdt = gradient(zpos, time);

% Second Partials:
d2Xdt = gradient (dXdt, time);
d2Ydt = gradient (dYdt, time);
d2Zdt = gradient (dZdt, time);

% Speed, Magnitude of Accel, Tangent Accel, Normal Accel:
speed = ((dXdt.^2 + dYdt.^2 + dZdt.^2).^(1/2));
magAccel = ((d2Xdt.^2 + d2Ydt.^2 + d2Zdt.^2).^(1/2));
tangentAccel = gradient(speed, time);
normalAccel = ((magAccel.^2) - (tangentAccel.^2)).^(1/2);

